    private void refreshCarsTableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_refreshCarsTableActionPerformed
        DefaultTableModel carsTable = (DefaultTableModel) CarsTablev.getModel();
        try {
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("select * from cars");
            int rowCount = -1;
            while (rs.next()) {
                rowCount++;
                String carId = rs.getString("carID");
                String carBrand = rs.getString("carBrand");
                String carModel = rs.getString("carModel");
                String ownerId = rs.getString("ownerId");
                carsTable.setRowCount(rowCount);
                carsTable.addRow(new Object[]{carId, carBrand, carModel, ownerId});
            }
            stmt.close();
        } catch (SQLException ex) {
            Logger.getLogger(DB2.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_refreshCarsTableActionPerformed

