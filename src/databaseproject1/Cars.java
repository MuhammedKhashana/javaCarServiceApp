    private void editCurrCarBtn(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editCurrCarBtn
        try {
            String brand = editCarBrand.getText();
            String model = editCarModel.getText();
            String ownerId = editOwnerID.getText();
            Statement stmt = con.createStatement();
            String editCar = "UPDATE cars SET carBrand = '" + brand + "', carModel = '" + model + "', ownerId = '" + ownerId + "' WHERE carID = '" + carSelectedRowID + "'";
            try {
                stmt.execute(editCar);
                editCarBrand.setText("");
                editCarModel.setText("");
                editOwnerID.setText("");
                CarsTablev.clearSelection();
                carSelectedRowID = -1;
            } catch (SQLException ex) {
                alertsLabel.setText("There's no Customer with that ID!");
                Timer timer = new Timer();
                timer.schedule(new TimerTask() {
                    @Override
                    public void run() {
                        alertsLabel.setText("");
                    }
                }, 2000);
            }

            refreshCarsTableActionPerformed(evt);
            stmt.close();
        } catch (SQLException ex) {
            Logger.getLogger(DB2.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_editCurrCarBtn

